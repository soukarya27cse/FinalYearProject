@st.cache_data(show_spinner=False)
def discover_workbooks(directory: str) -> dict[str, Path]:
    """
    Scan the workbooks directory and return {ticker_display_name: Path}.
    Filename format: RELIANCE_NS.xlsx → RELIANCE.NS
    """
    d = Path(directory)
    if not d.exists():
        return {}

    result = {}
    for f in sorted(d.glob("*.xlsx")):
        # Read the actual ticker symbol from col B row 3 — ground truth.
        ticker = _peek_ticker(f) or _ticker_from_stem(f.stem)
        result[ticker] = f
    return result


def _ticker_from_stem(stem: str) -> str:
    """Fallback: reverse the safe-filename transform on the workbook stem."""
    stem = stem.replace("AND", "&")
    parts = stem.rsplit("_", 1)
    if len(parts) == 2 and parts[1] in ("NS", "BO"):
        return f"{parts[0]}.{parts[1]}"
    return stem.replace("_", ".")


def _peek_ticker(path: Path) -> str | None:
    """Read the Ticker value from the first data row (col B, row 3)."""
    try:
        wb = load_workbook(str(path), data_only=True, read_only=True)
        if "Predictions" not in wb.sheetnames:
            wb.close()
            return None
        ws = wb["Predictions"]
        for row in ws.iter_rows(min_row=_DATA_START, max_row=_DATA_START + 5,
                                values_only=True):
            val = row[_COL_TICKER - 1]
            if val and str(val).strip():
                wb.close()
                return str(val).strip()
        wb.close()
    except Exception:
        pass
    return None


@st.cache_data(show_spinner=False)
def load_workbook_data(filepath: str) -> pd.DataFrame:
    """
    Read the Predictions sheet from one tracker workbook.
    Returns a clean DataFrame with columns:
        Date, Ticker, Actual, Predicted, Error_pct, Price_Features, Factor_Features
    Skips the two merged header rows and any rows that can't be parsed.
    """
    wb = load_workbook(filepath, data_only=True, read_only=True)

    if "Predictions" not in wb.sheetnames:
        wb.close()
        return pd.DataFrame()

    ws = wb["Predictions"]
    rows = []

    for row_idx, row in enumerate(ws.iter_rows(min_row=_DATA_START, values_only=True), start=_DATA_START):
        # row is a tuple indexed 0-based; col indices are 1-based in tracker
        try:
            date_val      = row[_COL_DATE     - 1]
            ticker_val    = row[_COL_TICKER   - 1]
            price_f_val   = row[_COL_PRICE_F  - 1]
            factor_f_val  = row[_COL_FACTOR_F - 1]
            actual_val    = row[_COL_ACTUAL   - 1]
            pred_val      = row[_COL_PRED     - 1]
            error_val     = row[_COL_ERROR    - 1]

            # Skip completely empty rows
            if all(v is None for v in (date_val, actual_val, pred_val)):
                continue

            # Parse date
            if isinstance(date_val, str):
                try:
                    date_val = pd.to_datetime(date_val).date()
                except Exception:
                    continue
            elif hasattr(date_val, "date"):
                date_val = date_val.date()
            elif date_val is None:
                continue

            # Coerce numerics
            actual_val = _safe_float(actual_val)
            pred_val   = _safe_float(pred_val)

            if actual_val is None or pred_val is None:
                continue

            # Recompute error from raw values — don't trust cached formula result
            error_pct = abs((actual_val - pred_val) / actual_val * 100) if actual_val != 0 else None
            signed_err = ((actual_val - pred_val) / actual_val * 100) if actual_val != 0 else 0

            rows.append({
                "Date"            : date_val,
                "Ticker"          : str(ticker_val) if ticker_val else "",
                "Actual"          : actual_val,
                "Predicted"       : pred_val,
                "Error_pct"       : error_pct,
                "Signed_Error"    : signed_err,
                "Price_Features"  : str(price_f_val)  if price_f_val  else "",
                "Factor_Features" : str(factor_f_val) if factor_f_val else "",
            })
        except (IndexError, TypeError):
            continue

    wb.close()

    if not rows:
        return pd.DataFrame()

    df = pd.DataFrame(rows)
    df["Date"] = pd.to_datetime(df["Date"])
    df = df.sort_values("Date").reset_index(drop=True)
    return df


def _safe_float(val) -> float | None:
    if val is None:
        return None
    try:
        f = float(val)
        return None if (np.isnan(f) or np.isinf(f)) else f
    except (TypeError, ValueError):
        return None


