def _resolve_workbook_dir() -> Path:
    """
    Priority:
      1. CLI arg  --workbooks <path>
      2. Streamlit query param  ?workbooks=<path>
      3. workbooks/ sibling of this script
    """
    # CLI args passed after  `streamlit run dashboard.py --`
    parser = argparse.ArgumentParser(add_help=False)
    parser.add_argument("--workbooks", default=None)
    known, _ = parser.parse_known_args()
    if known.workbooks:
        return Path(known.workbooks)

    params = st.query_params
    if "workbooks" in params:
        return Path(params["workbooks"])

    return Path(__file__).parent / "workbooks"


WORKBOOK_DIR = _resolve_workbook_dir()

