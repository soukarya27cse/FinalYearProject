def render_sidebar(workbooks: dict[str, Path]) -> tuple[str | None, pd.DataFrame | None]:
    """Render sidebar controls. Returns (selected_ticker, filtered_df)."""

    with st.sidebar:
        st.markdown("""
<div style="padding:1.2rem 0 1rem">
  <div style="font-family:'Syne',sans-serif;font-size:1.4rem;font-weight:800;
              background:linear-gradient(90deg,#38bdf8,#818cf8);
              -webkit-background-clip:text;-webkit-text-fill-color:transparent;
              background-clip:text">
    📈 Ticker-Teller
  </div>
  <div style="font-family:'DM Mono',monospace;font-size:0.62rem;
              color:#475569;letter-spacing:0.1em;margin-top:0.2rem">
    NIFTY 100  ·  PREDICTION DASHBOARD
  </div>
</div>
""", unsafe_allow_html=True)

        st.divider()

        if not workbooks:
            st.error(f"No workbooks found in:\n`{WORKBOOK_DIR}`\n\nRun the tracker first.")
            return None, None

        # ── Ticker selector ────────────────────────────────────────────────────
        st.markdown('<div class="sidebar-label">Select Company</div>',
                    unsafe_allow_html=True)
        tickers = sorted(workbooks.keys())
        selected = st.selectbox("", tickers, label_visibility="collapsed",
                                key="ticker_select")

        # ── Load data ─────────────────────────────────────────────────────────
        df_full = load_workbook_data(str(workbooks[selected]))

        if df_full.empty:
            st.warning("No data rows found in this workbook.")
            return selected, None

        # ── Date range filter ──────────────────────────────────────────────────
        st.markdown('<div class="sidebar-label" style="margin-top:1.2rem">Date Range</div>',
                    unsafe_allow_html=True)

        min_date = df_full["Date"].min().date()
        max_date = df_full["Date"].max().date()

        date_from = st.date_input("From", value=min_date,
                                  min_value=min_date, max_value=max_date,
                                  key="date_from", label_visibility="collapsed")
        date_to   = st.date_input("To",   value=max_date,
                                  min_value=min_date, max_value=max_date,
                                  key="date_to",   label_visibility="collapsed")

        if date_from > date_to:
            st.warning("'From' date is after 'To' date — swapping.")
            date_from, date_to = date_to, date_from

        df = df_full[
            (df_full["Date"].dt.date >= date_from) &
            (df_full["Date"].dt.date <= date_to)
        ].reset_index(drop=True)

        st.divider()

        # ── Workbook info ──────────────────────────────────────────────────────
        st.markdown(f"""
<div style="font-family:'DM Mono',monospace;font-size:0.68rem;
            line-height:2;color:#475569">
  <span style="color:#38bdf8">●</span>  {len(df_full)} total rows<br>
  <span style="color:#818cf8">●</span>  {len(df)} after filter<br>
  <span style="color:#4ade80">●</span>  {min_date}  →  {max_date}
</div>
""", unsafe_allow_html=True)

        return selected, df


