def build_cumulative_return_chart(df: pd.DataFrame, ticker: str) -> go.Figure:
    """Cumulative return: actual vs predicted, base = first day."""
    if len(df) < 2:
        return go.Figure()

    base_a = df["Actual"].iloc[0]
    base_p = df["Predicted"].iloc[0]
    cum_actual    = (df["Actual"]    / base_a - 1) * 100
    cum_predicted = (df["Predicted"] / base_p - 1) * 100

    fig = go.Figure()
    fig.add_trace(go.Scatter(
        x             = df["Date"],
        y             = cum_actual,
        mode          = "lines",
        name          = "Actual Return",
        line          = dict(color="#38bdf8", width=2.5),
        hovertemplate = "<b>Actual</b>: %{y:.2f}%<extra></extra>",
    ))
    fig.add_trace(go.Scatter(
        x             = df["Date"],
        y             = cum_predicted,
        mode          = "lines",
        name          = "Predicted Return",
        line          = dict(color="#818cf8", width=2, dash="dash"),
        hovertemplate = "<b>Predicted</b>: %{y:.2f}%<extra></extra>",
    ))
    fig.add_hline(y=0, line_color="rgba(148,163,184,0.25)", line_dash="solid")

    fig.update_layout(
        title  = dict(
            text = f"<b>{ticker}</b>  —  Cumulative Return (%)",
            font = dict(family="Syne, sans-serif", size=16, color="#f1f5f9"),
            x    = 0.01,
        ),
        height = 350,
        xaxis  = dict(title="Date", tickformat="%b %d, %Y", **_AXIS_STYLE),
        yaxis  = dict(title="Cumulative Return (%)", **_AXIS_STYLE),
        **_LAYOUT_BASE,
    )
    return fig


