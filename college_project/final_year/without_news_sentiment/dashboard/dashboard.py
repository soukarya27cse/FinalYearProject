# SUMMARY STATISTICS
# ═══════════════════════════════════════════════════════════════════════════════

def compute_stats(df: pd.DataFrame) -> dict:
    errors = df["Error_pct"].dropna()
    signed = df["Signed_Error"].dropna()

    correct_direction = (signed > 0).sum()
    dir_acc = correct_direction / len(signed) * 100 if len(signed) > 0 else 0

    price_change_pct = (
        (df["Actual"].iloc[-1] - df["Actual"].iloc[0]) / df["Actual"].iloc[0] * 100
        if len(df) > 1 else 0
    )

    return {
        "days"          : len(df),
        "avg_error"     : errors.mean(),
        "median_error"  : errors.median(),
        "max_error"     : errors.max(),
        "min_error"     : errors.min(),
        "std_error"     : errors.std(),
        "dir_accuracy"  : dir_acc,
        "total_return"  : price_change_pct,
        "last_actual"   : df["Actual"].iloc[-1],
        "last_predicted": df["Predicted"].iloc[-1],
    }


