# MAIN APP
# ═══════════════════════════════════════════════════════════════════════════════

def main() -> None:

    # ── Hero banner ────────────────────────────────────────────────────────────
    st.markdown("""
<div class="tt-hero">
  <div class="tt-hero-title">📈 Ticker-Teller Dashboard</div>
  <div class="tt-hero-sub">
    NIFTY 100  ·  CNN → BILSTM → ATTENTION  ·  MONTE CARLO UNCERTAINTY ESTIMATION
  </div>
</div>
""", unsafe_allow_html=True)

    # ── Discover workbooks ─────────────────────────────────────────────────────
    workbooks = discover_workbooks(str(WORKBOOK_DIR))

    # ── Sidebar ────────────────────────────────────────────────────────────────
    ticker, df = render_sidebar(workbooks)

    if ticker is None:
        st.info(
            f"📂  Workbooks directory: **`{WORKBOOK_DIR}`**\n\n"
            "Run `python3 nifty100_tracker.py --once` to generate prediction data, "
            "then refresh this page."
        )
        return

    if df is None or df.empty:
        st.warning(
            f"No data available for **{ticker}** in the selected date range. "
            "Try widening the date filter."
        )
        return

    stats = compute_stats(df)

    # ── KPI row ────────────────────────────────────────────────────────────────
    st.markdown('<div class="sec-hdr">📊 Summary Statistics</div>',
                unsafe_allow_html=True)

    k1, k2, k3, k4, k5, k6 = st.columns(6)
    k1.metric("LAST ACTUAL PRICE",     f"₹{stats['last_actual']:,.2f}")
    k2.metric("LAST PREDICTED PRICE",  f"₹{stats['last_predicted']:,.2f}")
    k3.metric("AVG ERROR (%)",          f"{stats['avg_error']:.2f}%")
    k4.metric("MEDIAN ERROR (%)",       f"{stats['median_error']:.2f}%")
    k5.metric("DIRECTIONAL ACCURACY",  f"{stats['dir_accuracy']:.1f}%")
    k6.metric("TOTAL RETURN (ACTUAL)",
              f"{stats['total_return']:+.2f}%",
              delta=f"{stats['total_return']:+.2f}%")

    # ── Error stats row ────────────────────────────────────────────────────────
    e1, e2, e3, e4 = st.columns(4)
    e1.metric("MAX ERROR (%)",    f"{stats['max_error']:.2f}%")
    e2.metric("MIN ERROR (%)",    f"{stats['min_error']:.2f}%")
    e3.metric("STD DEV ERROR",    f"{stats['std_error']:.2f}%")
    e4.metric("DAYS ANALYSED",    str(stats["days"]))

    # ── Main chart tabs ────────────────────────────────────────────────────────
    st.markdown('<div class="sec-hdr">📈 Charts</div>', unsafe_allow_html=True)

    tab1, tab2, tab3, tab4, tab5 = st.tabs([
        "📉  PRICE",
        "❌  ERROR",
        "🔵  SCATTER",
        "📈  CUMULATIVE RETURN",
        "🗂️  DATA TABLE",
    ])

    with tab1:
        st.plotly_chart(
            build_price_chart(df, ticker),
            use_container_width=True,
        )
        st.caption(
            f"Blue = Actual closing price  ·  Pink dashed = Model predicted price  ·  "
            f"{stats['days']} trading days shown"
        )

    with tab2:
        st.plotly_chart(
            build_error_chart(df, ticker),
            use_container_width=True,
        )
        st.caption(
            "🟢 Green bar = model underestimated (Actual > Predicted)  ·  "
            "🔴 Red bar = model overestimated (Actual < Predicted)  ·  "
            "Yellow dot line = average error"
        )

    with tab3:
        st.plotly_chart(
            build_scatter_chart(df, ticker),
            use_container_width=True,
        )
        st.caption(
            "Points on the diagonal = perfect predictions.  "
            "🟢 Green = model underestimated  ·  🔴 Red = model overestimated"
        )

    with tab4:
        if stats["days"] >= 2:
            st.plotly_chart(
                build_cumulative_return_chart(df, ticker),
                use_container_width=True,
            )
            st.caption(
                "Cumulative % return from the first day in the selected range.  "
                "Blue = actual price path  ·  Purple dashed = predicted price path."
            )
        else:
            st.info("Need at least 2 data points to compute cumulative return.")

    with tab5:
        st.markdown(
            '<div class="sec-hdr" style="margin-top:0.5rem">Raw Prediction Data</div>',
            unsafe_allow_html=True,
        )

        # Build display table (hide long feature text by default)
        show_features = st.checkbox("Show model feature columns (verbose)", value=False)

        display_cols = ["Date", "Ticker", "Actual", "Predicted", "Error_pct", "Signed_Error"]
        if show_features:
            display_cols += ["Price_Features", "Factor_Features"]

        df_display = df[display_cols].copy()
        df_display["Date"]       = df_display["Date"].dt.strftime("%Y-%m-%d")
        df_display["Actual"]     = df_display["Actual"].map("₹{:,.2f}".format)
        df_display["Predicted"]  = df_display["Predicted"].map("₹{:,.2f}".format)
        df_display["Error_pct"]  = df_display["Error_pct"].map("{:.2f}%".format)
        df_display["Signed_Error"] = df_display["Signed_Error"].map("{:+.2f}%".format)

        df_display.columns = (
            ["Date", "Ticker", "Actual Price", "Predicted Price",
             "Error (%)", "Signed Error (%)"]
            + (["Price Features", "Factor Features"] if show_features else [])
        )

        st.dataframe(df_display, use_container_width=True, hide_index=True)

        # CSV download
        csv = df[display_cols].to_csv(index=False).encode("utf-8")
        st.download_button(
            label    = "⬇️  Download as CSV",
            data     = csv,
            file_name= f"{ticker.replace('.', '_')}_predictions.csv",
            mime     = "text/csv",
        )

    # ── Multi-ticker comparison (bonus) ───────────────────────────────────────
    st.markdown('<div class="sec-hdr">🔁 Compare Multiple Tickers</div>',
                unsafe_allow_html=True)
    st.caption(
        "Select additional tickers to overlay their actual prices on one chart."
    )

    all_tickers = sorted(workbooks.keys())
    compare_tickers = st.multiselect(
        "Add tickers to compare",
        options  = [t for t in all_tickers if t != ticker],
        default  = [],
        key      = "compare_select",
        label_visibility = "collapsed",
    )

    if compare_tickers:
        fig_cmp = go.Figure()

        # Include the primary ticker first
        for t in [ticker] + compare_tickers:
            df_t = load_workbook_data(str(workbooks[t]))
            if df_t.empty:
                continue
            fig_cmp.add_trace(go.Scatter(
                x             = df_t["Date"],
                y             = df_t["Actual"],
                mode          = "lines",
                name          = t,
                hovertemplate = f"<b>{t}</b>: ₹%{{y:,.2f}}<extra></extra>",
            ))

        fig_cmp.update_layout(
            title  = dict(
                text = "Actual Price Comparison",
                font = dict(family="Syne, sans-serif", size=16, color="#f1f5f9"),
                x    = 0.01,
            ),
            height = 380,
            xaxis  = dict(title="Date", tickformat="%b %d, %Y", **_AXIS_STYLE),
            yaxis  = dict(title="Price (₹)", **_AXIS_STYLE),
            **_LAYOUT_BASE,
        )
        st.plotly_chart(fig_cmp, use_container_width=True)

    # ── Footer ─────────────────────────────────────────────────────────────────
    st.divider()
    st.markdown("""
<div style="text-align:center;font-family:'DM Mono',monospace;
            font-size:0.65rem;color:#334155;padding:0.5rem 0 1rem">
  Ticker-Teller  ·  CNN → BiLSTM → Attention  ×  Monte Carlo  ·  NSE NIFTY 100<br>
  For educational and research use only. Not financial advice.
</div>
""", unsafe_allow_html=True)


if __name__ == "__main__":
    main()
