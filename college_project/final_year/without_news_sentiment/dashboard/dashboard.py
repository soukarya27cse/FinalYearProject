def build_price_chart(df: pd.DataFrame, ticker: str) -> go.Figure:
    """Actual vs Predicted price overlay with gap shading."""
    fig = go.Figure()

    # Confidence gap fill (between actual and predicted)
    fig.add_trace(go.Scatter(
        x    = pd.concat([df["Date"], df["Date"][::-1]]),
        y    = pd.concat([df["Actual"], df["Predicted"][::-1]]),
        fill = "toself",
        fillcolor = "rgba(56,189,248,0.06)",
        line = dict(color="rgba(0,0,0,0)"),
        hoverinfo = "skip",
        showlegend= False,
        name = "Gap",
    ))

    # Actual price
    fig.add_trace(go.Scatter(
        x          = df["Date"],
        y          = df["Actual"],
        mode       = "lines+markers",
        name       = "Actual Price",
        line       = dict(color="#38bdf8", width=2.5),
        marker     = dict(size=5, color="#38bdf8",
                          line=dict(color="#080f1c", width=1)),
        hovertemplate = "<b>Actual</b>: ₹%{y:,.2f}<extra></extra>",
    ))

    # Predicted price
    fig.add_trace(go.Scatter(
        x          = df["Date"],
        y          = df["Predicted"],
        mode       = "lines+markers",
        name       = "Predicted Price",
        line       = dict(color="#f472b6", width=2, dash="dash"),
        marker     = dict(size=5, color="#f472b6",
                          line=dict(color="#080f1c", width=1)),
        hovertemplate = "<b>Predicted</b>: ₹%{y:,.2f}<extra></extra>",
    ))

    fig.update_layout(
        title = dict(
            text      = f"<b>{ticker}</b>  —  Actual vs Predicted Price",
            font      = dict(family="Syne, sans-serif", size=16, color="#f1f5f9"),
            x         = 0.01,
        ),
        height     = 420,
        xaxis      = dict(title="Date", tickformat="%b %d, %Y", **_AXIS_STYLE),
        yaxis      = dict(title="Price (₹)", **_AXIS_STYLE),
        **_LAYOUT_BASE,
    )
    return fig


def build_error_chart(df: pd.DataFrame, ticker: str) -> go.Figure:
    """Daily absolute error % as a bar chart with signed colour coding."""
    colours = [
        "#4ade80" if s >= 0 else "#f87171"
        for s in df["Signed_Error"]
    ]

    fig = go.Figure()
    fig.add_trace(go.Bar(
        x             = df["Date"],
        y             = df["Error_pct"],
        marker_color  = colours,
        marker_line   = dict(color="rgba(0,0,0,0)", width=0),
        name          = "Error (%)",
        hovertemplate = "<b>Error</b>: %{y:.2f}%<extra></extra>",
        opacity       = 0.85,
    ))

    # Average error line
    avg_err = df["Error_pct"].mean()
    fig.add_hline(
        y                    = avg_err,
        line_dash            = "dot",
        line_color           = "rgba(251,191,36,0.7)",
        annotation_text      = f" Avg {avg_err:.2f}%",
        annotation_font      = dict(color="#fbbf24", size=11),
        annotation_position  = "top right",
    )

    fig.update_layout(
        title = dict(
            text  = f"<b>{ticker}</b>  —  Prediction Error (%) per Day",
            font  = dict(family="Syne, sans-serif", size=16, color="#f1f5f9"),
            x     = 0.01,
        ),
        height = 320,
        xaxis  = dict(title="Date", tickformat="%b %d, %Y", **_AXIS_STYLE),
        yaxis  = dict(title="Absolute Error (%)", **_AXIS_STYLE),
        **_LAYOUT_BASE,
    )
    return fig


